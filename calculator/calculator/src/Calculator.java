public class Calculator {
   /**
    * This method is used to sum two integers numbers
    * @param numberOne this is the first parameter of the method
    * @param numberTwo this is the second parameter of the method
    * @return int the result of this method is the sum of the two numbers.
    */

    public int sum (int numberOne, int numberTwo) {
        return numberOne + numberTwo;     
    }
}
